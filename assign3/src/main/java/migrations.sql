CREATE TABLE IF NOT EXISTS factory.users (
    ID INT NOT NULL AUTO_INCREMENT,
    USERN VARCHAR(20),
    PASS VARCHAR(20),
    PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS factory.searches (
    ID INT NOT NULL AUTO_INCREMENT,
    USER_ID INT,
    TS TIMESTAMP,
    LOC VARCHAR(50),
    LAT DOUBLE,
    LNG DOUBLE,
    PRIMARY KEY (ID),
    FOREIGN KEY (USER_ID) REFERENCES users(ID)
);

CREATE TABLE IF NOT EXISTS factory.sessions (
    ID INT NOT NULL,
    USER_ID INT,
    PRIMARY KEY (ID),
    FOREIGN KEY (USER_ID) REFERENCES users(ID)
);
